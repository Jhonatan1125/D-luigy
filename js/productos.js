
function productos (){

  var color1 = document.getElementById('color1').value;
  var talla1 = document.getElementById('talla1').value;
  

    if(talla1 === '' || color1 === '' ){


            swal({
            title: 'Incorrecto',
            input: 'checkbox',
            icon: "error",
            text: "Los campos son obligatorios"


            });

            return false;
     } else{
     	 swal({
                  title: "¿Estas seguro?",
                  text: "El producto será comprado",
                  icon: "warning",
                  buttons: true,
                  buttons: ["Cancelar", "Comprar"]

                })
                .then((willDelete) => {
                  if (willDelete) {
                   swal("Listo", "Se ha enviado el pedido", "success");
                  }
                });
     }
}


